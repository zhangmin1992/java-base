public class TCanUseOthers {
    public boolean canUseCard;
    public boolean canBuyGoods;
    public boolean canBuyDiscountCards;
    public boolean canRefund;
    public boolean canMigrate;

    public boolean isCanUseCard() {
        return canUseCard;
    }

    public void setCanUseCard(boolean canUseCard) {
        this.canUseCard = canUseCard;
    }

    public boolean isCanBuyGoods() {
        return canBuyGoods;
    }

    public void setCanBuyGoods(boolean canBuyGoods) {
        this.canBuyGoods = canBuyGoods;
    }

    public boolean isCanBuyDiscountCards() {
        return canBuyDiscountCards;
    }

    public void setCanBuyDiscountCards(boolean canBuyDiscountCards) {
        this.canBuyDiscountCards = canBuyDiscountCards;
    }

    public boolean isCanRefund() {
        return canRefund;
    }

    public void setCanRefund(boolean canRefund) {
        this.canRefund = canRefund;
    }

    public boolean isCanMigrate() {
        return canMigrate;
    }

    public void setCanMigrate(boolean canMigrate) {
        this.canMigrate = canMigrate;
    }
}
